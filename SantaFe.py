def newTrail(trail):
    newTrail = []
    for row in trail:
        newTrail.append(list(row))
    return newTrail

def printTrail(trail):
    for row in trail:
        print(str(row).replace(", ", " ").replace("0", " "))

class SantaFeAgent():

    def __init__(self, agent):
        self.trail = newTrail(TRAIL)
        self.agent = agent
        self.direction = "E"
        self.x = 0
        self.y = 0
        self.foodEaten = 0
        self.evaluations = 0

    def R(self):
        self.direction = {"N":"E",
                          "E":"S",
                          "S":"W",
                          "W":"N"}[self.direction]
        self.evaluations+=1
        
    def L(self):
        self.direction = {"N":"W",
                          "E":"N",
                          "S":"E",
                          "W":"S"}[self.direction]
        self.evaluations+=1

    def F(self):
        if self.direction == "N":
            self.y-=1
        elif self.direction == "E":
            self.x+=1
        elif self.direction == "S":
            self.y+=1
        elif self.direction == "W":
            self.x-=1
        self.evaluations+=1

    def finished(self):
        stillGoing = 0<=self.x<=31 and 0<=self.y<=31 and self.foodEaten<90 and self.evaluations<600
        return not stillGoing

    def foodAhead(self):
        x = self.x
        y = self.y
        if self.direction == "N":
            y-=1
        elif self.direction == "E":
            x+=1
        elif self.direction == "S":
            y+=1
        elif self.direction == "W":
            x-=1
        try:
            return self.trail[y][x]
        except IndexError:
            return 0

    def fitness(self):
        fitness = 0
        while not self.finished():
            fitness += self.trail[self.y][self.x]
            self.trail[self.y][self.x] = 0
            instruction = "self."+self.agent.think(self.foodAhead())+"()"
            #print(instruction)
            exec(instruction)
        return fitness














        
